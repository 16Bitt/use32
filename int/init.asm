	use32

macro ISR_BLANK number {
isr#number:
	cli
	push dword number
	jmp isr_common_stub
}

macro ISR_ERR number {
isr#number:
	add esp, 1
	push dword number
	jmp isr_common_stub
}

isr_common_stub:
	pusha			;REGS
	mov ax, ds
	push eax		;REGS, EAX

	mov ax, 0x10
	mov ds, ax
	mov es, ax
	mov fs, ax
	mov gs, ax
	push esp		;REGS, EAX, ESP

	call isr_handler

	add esp, 4		;REGS, EAX
	pop ebx			;REGS
	mov ds, bx
	mov es, bx
	mov fs, bx
	mov gs, bx
	popa			;--
	add esp, 4

	iret

macro outb port, value {
	mov al, value
	out port, al
	call io_wait
}

io_wait:
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	in al, 0x10 + 0xC
	ret

init_idt:
	push ebp
	mov ebp, esp
	
	;Remap everything in the interrupt controller (messy)
	outb 0x20, 0x11
	outb 0xA0, 0x11
	outb 0x21, 0x20
	outb 0xA1, 0x28
	outb 0x21, 0x04
	outb 0xA1, 0x02
	outb 0x21, 0x01
	outb 0xA1, 0x01
	outb 0x21, 0
	outb 0xA1, 0

	;Initialize a blank jump table
	push 4 * 256
	call malloc
	mov [idt_handlers], eax
	push eax
	push 0
	push 4 * 256
	call memset
	
	;Initialize the IDT
	lidt [idt_main_descriptor]
	
	;Map common handlers
	push 0
	push err_div_by_zero
	call int_map
	push 1
	push err_debug
	call int_map
	push 0xD
	push err_gpf
	call int_map
	push 0x6
	push err_instruction
	call int_map
	push 14
	push err_page_fault
	call int_map

	mov esp, ebp
	pop ebp
	ret

idt_handlers: dd 0

idt_main_descriptor:
	dw (8 * 256) - 1	;idt limit
	dd interrupt_table	;idt base

macro MK_INT_ENTRY  num* {
	dw (isr#num shl 16) shr 16
	dw 0x08
	db 0
	db (0x8E or 0x60)
	dw (isr#num shr 16)
}

